module.exports = async function() {
    app.get("/logout", core.auth, (req, res) => {
        if (!req.session.userinfo) {
            res.statusCode = 302;
            res.setHeader('Location', '/');
            return res.end();
        }
        req.session.destroy(() => {
            res.statusCode = 302;
            res.setHeader('Location', '/');
            return res.end();
        });
    });
}
